   SUBROUTINE Sort ( N, Arr )


      ! This routine sorts an array in ascending order.
      ! Based on straight insertion routine in "Numerical Recipes in Fortran"

   USE                                NWTC_Library


      ! Argument declarations.

   INTEGER, INTENT(IN)             :: N

   REAL(ReKi), INTENT(INOUT)       :: Arr (N)


      ! Local declarations.

   REAL(ReKi)                      :: A

   INTEGER                         :: I
   INTEGER                         :: J



   Outer: DO J = 2, N
      A = Arr( J )
      Inner: DO I = J-1, 1, -1
         IF ( Arr( I ) <= A ) THEN
           Arr( I + 1 ) = A
          CYCLE Outer
        ENDIF
        Arr( I + 1 ) = Arr( I )
      ENDDO Inner  ! I
      I = 0
      Arr( I + 1 ) = A
   ENDDO Outer

   RETURN
   END SUBROUTINE Sort ! ( N, Arr )
